import { Queue, Worker } from 'bullmq';
import Parser from 'rss-parser';
import { Prisma, PrismaClient, ShowType, SourceCategory } from '@prisma/client';
import { contentHashFromText, normalizeUrl } from '../../../lib/pipeline/canonicalize';
import { shouldJoinCluster } from '../../../lib/pipeline/dedupCluster';
import { summarizeCluster } from '../../../lib/pipeline/summarize';
import { computeClusterScore } from '../../../lib/ranking/score';
import { invalidateCached } from '../../../lib/ranking/cache';
import { generatePodcastScript, VoiceMode } from '../../../lib/podcast/template';
import { uploadAudioBuffer } from '../services/storage';
import { evaluateClusterQuality } from '../../../lib/evaluation/clusterQuality';
import { evaluateSummaryQuality } from '../../../lib/evaluation/summaryQuality';
import { calculateRankingDrift } from '../../../lib/evaluation/rankingDrift';
import { scoreSourceHealth } from '../../../lib/reputation/sourceHealth';
import { buildInterestVector } from '../../../lib/personalization/interestVector';
import { assignVariant } from '../../../lib/experiments/assignment';
